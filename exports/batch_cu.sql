
Basic Profile

Name: Cole Quinn

Nickname: Cole World

Birthdate: 3/17/2000

Age: 25

Hometown: Orlando, Florida

Debut: May Week 2 2025 Saturday, SFPW 2

Height/Weight: 6’1” - 210 LBs

Signature Move: Coled Snap(Hangman’s Neckbreaker)

Finishing Move: The Quinn-tervention(Standing DDT to Kneeling Opponent)

Overview: Cole Quinn is proof that heart sometimes matters more than highlights. He’s the guy who might not win often — or at all — but he shows up every single time like it’s his WrestleMania moment.

Nicknamed “Cole World”, he’s a walking contradiction: confident but clumsy, cool in theory but chaotic in execution. He’s the kind of wrestler who makes the audience grin just by existing — not because he’s the best, but because he believes he could be.

Cole’s the self-proclaimed “engine” of the World Beaters — even if KJ Boom and Jay Pierce don’t always agree. While his teammates carry the swagger, Cole carries the spark. He’s the one who takes the beatings, eats the pins, and still gets up smiling, throwing finger guns and shouting “You just got Coled!”

His signature Coled Snap always gets a pop — half because of the move, half because of the name. And when he manages to hit his finisher, The Quinn-tervention, the arena loses its mind. Not because they think he’ll win… but because he might.

Cole’s wrestling style is scrappy, desperate, and creative. He compensates for his lack of polish with sheer unpredictability — wild counters, random bursts of energy, and a knack for surviving just long enough to make people believe in him.

He’s the embodiment of “fake it till you make it.”

And maybe, just maybe, he’s been faking it long enough to start making it.

“It’s a Cole World, baby — I’m just living in it.”

Card Position: Jobber

Alignment: Tweener

Stable/Tag Team: World Beaters(Cole Quinn + KJ Boom & Jay Pierce)

Wrestling style: Grappler/Brawler

Personality Traits: Quirky, Goofy, Resilient

Ring Presence: When Cole Quinn walks out, it’s impossible not to smile. His energy is contagious — part hype man, part kid-on-a-sugar-rush, part guy-who-still-can’t-believe-he’s-here. His entrance isn’t polished, but it’s full of heart: finger guns, big grins, maybe a few missteps, but the crowd eats it up because it’s so him.

Cole doesn’t act like he’s performing; he acts like he’s participating. He’s living his dream in real time, and the audience feels that. When he points to the crowd or hypes himself up, it’s not forced — it’s genuine excitement from a dude who grew up watching the legends and now gets to play on their stage.
